import { createHash } from 'node:crypto';
import { readFile, writeFile } from 'node:fs/promises';
import { createRequire } from 'node:module';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import protobuf from 'protobufjs';

const require = createRequire(import.meta.url);
const pbjs = require('protobufjs-cli/pbjs');

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const webDir = path.resolve(scriptDir, '..');
const repoDir = path.resolve(webDir, '..');
const protoDir = path.join(repoDir, 'internal/protocol/proto');
const outputFile = path.join(webDir, 'src/protocol/generated.ts');
const runtimeFile = path.join(webDir, 'src/protocol/generated-runtime.js');
const runtimeTypesFile = path.join(webDir, 'src/protocol/generated-runtime.d.ts');
const protoFiles = ['common.proto', 'client.proto', 'server.proto', 'game.proto', 'message.proto']
  .map((name) => path.join(protoDir, name));

const root = new protobuf.Root();
root.resolvePath = (_origin, target) => path.resolve(repoDir, target);
root.loadSync(protoFiles, { keepCase: true });
root.resolveAll();

const namespace = root.lookup('protocol');
const nested = namespace.nested ?? {};
const messageType = root.lookupEnum('protocol.MessageType');
const messageEntries = Object.entries(messageType.values)
  .sort(([, left], [, right]) => left - right);
const messageNames = messageEntries
  .filter(([name]) => name !== 'MSG_UNKNOWN')
  .map(([name, value]) => ({
    enumName: name,
    value,
    wireName: name.slice(4).toLowerCase(),
    propertyName: pascalCase(name.slice(4))
  }));

const schemaHash = createHash('sha256');
for (const file of protoFiles) schemaHash.update(await readFile(file));
const schemaDigest = schemaHash.digest('hex');

const types = Object.values(nested)
  .filter((value) => value instanceof protobuf.Type)
  .sort((left, right) => left.name.localeCompare(right.name));
const enums = Object.values(nested)
  .filter((value) => value instanceof protobuf.Enum)
  .sort((left, right) => left.name.localeCompare(right.name));
const optionalFields = new Set([
  'Message.event',
  'Message.command',
  'PlayerInfo.is_bot',
  'ReconnectPayload.token',
  'ReconnectPayload.player_id',
  'ConnectedPayload.reconnect_token',
  'ConnectedPayload.web_session_ticket',
  'ConnectedPayload.reconnect_available',
  'ReconnectedPayload.room_code',
  'ReconnectedPayload.game_state',
  'ReconnectedPayload.reconnect_token',
  'ReconnectedPayload.web_session_ticket',
  'ProtocolRejectedPayload.min_client_version',
  'ErrorPayload.command_type',
  'ErrorPayload.request_id',
  'GameStateDTO.snapshot_version',
  'GameStateDTO.game_id',
  'GameStateDTO.bottom_cards_revealed',
  'GameStateDTO.turn_id',
  'GameStateDTO.turn_deadline_ms',
  'GameStateDTO.server_time_ms',
  'GameStateDTO.last_player_name',
  'GameStateDTO.last_hand_type',
  'GameStateDTO.is_grab',
  'GameStateDTO.multiplier',
  'GameStateDTO.base_score',
  'GameStateDTO.played_cards',
  'GameStateDTO.settlement',
  'ChatPayload.sender_id',
  'ChatPayload.sender_name',
  'ChatPayload.time',
  'ChatPayload.is_system',
  'ChatPayload.message_id',
  'ChatPayload.room_code',
  'ChatPayload.game_id',
  'ChatPayload.server_time'
]);

const lines = [
  '// Code generated by scripts/generate-protocol.mjs; DO NOT EDIT.',
  `// Canonical schema SHA-256: ${schemaDigest}`,
  '',
  'export enum MessageType {'
];

for (const [name, value] of messageEntries) lines.push(`  ${name} = ${value},`);
lines.push('}', '');

for (const enumType of enums) {
  if (enumType.name === 'MessageType') continue;
  lines.push(`export enum ${enumType.name} {`);
  for (const [name, value] of Object.entries(enumType.values)) lines.push(`  ${name} = ${value},`);
  lines.push('}', '');
}

for (const type of types) {
  lines.push(`export interface ${type.name} {`);
  for (const field of type.fieldsArray.sort((left, right) => left.id - right.id)) {
    const baseType = typescriptType(field);
    const optional = optionalFields.has(`${type.name}.${field.name}`) ? '?' : '';
    lines.push(`  ${field.name}${optional}: ${field.repeated ? `${baseType}[]` : baseType};`);
  }
  lines.push('}', '');
}

lines.push(
  'export const MsgType = {',
  ...messageNames.map(({ propertyName, wireName }) => `  ${propertyName}: '${wireName}',`),
  '} as const;',
  '',
  'export type MessageName = (typeof MsgType)[keyof typeof MsgType];',
  'export type EmptyPayload = Record<string, never>;',
  '',
  'export const MESSAGE_TYPE_BY_NAME: Readonly<Record<MessageName, MessageType>> = {',
  ...messageNames.map(({ enumName, wireName }) => `  ${wireName}: MessageType.${enumName},`),
  '};',
  '',
  'export const MESSAGE_NAME_BY_TYPE: Readonly<Record<number, MessageName>> = {',
  ...messageNames.map(({ value, wireName }) => `  ${value}: '${wireName}',`),
  '};',
  '',
  'export const PAYLOAD_TYPE_BY_NAME: Readonly<Record<MessageName, string | null>> = {',
  ...messageNames.map(({ wireName }) => {
    const candidate = `${pascalCase(wireName)}Payload`;
    return `  ${wireName}: ${nested[candidate] instanceof protobuf.Type ? `'protocol.${candidate}'` : 'null'},`;
  }),
  '};',
  '',
  'export const REQUIRED_FIELDS_BY_NAME: Readonly<Partial<Record<MessageName, readonly string[]>>> = {',
  ...messageNames.flatMap(({ wireName }) => {
    const candidateName = `${pascalCase(wireName)}Payload`;
    const candidate = nested[candidateName];
    if (!(candidate instanceof protobuf.Type)) return [];
    const required = candidate.fieldsArray
      .sort((left, right) => left.id - right.id)
      .filter((field) => !optionalFields.has(`${candidateName}.${field.name}`))
      .map((field) => `'${field.name}'`);
    return required.length > 0 ? [`  ${wireName}: [${required.join(', ')}],`] : [];
  }),
  '};',
  '',
  'export interface PayloadByName {'
);

for (const { wireName } of messageNames) {
  const candidate = `${pascalCase(wireName)}Payload`;
  lines.push(`  ${wireName}: ${nested[candidate] instanceof protobuf.Type ? candidate : 'EmptyPayload'};`);
}

lines.push(
  '}',
  '',
  'export type ProtocolMessage<N extends MessageName = MessageName> = {',
  '  [K in N]: { type: K; payload: PayloadByName[K]; event?: EventMeta; command?: CommandMeta }',
  '}[N];',
  '',
  'export type Int64FieldMetadata =',
  "  | { readonly name: string; readonly repeated: boolean; readonly kind: 'int64' }",
  "  | { readonly name: string; readonly repeated: boolean; readonly kind: 'message'; readonly message_type: string };",
  '',
  'export const INT64_FIELD_METADATA_BY_TYPE: Readonly<Record<string, readonly Int64FieldMetadata[]>> = {'
);

const typesWithInt64 = new Set(
  types.filter((type) => type.fieldsArray.some((field) => isInt64(field.type)))
);
let metadataChanged = true;
while (metadataChanged) {
  metadataChanged = false;
  for (const type of types) {
    if (typesWithInt64.has(type)) continue;
    if (type.fieldsArray.some((field) => field.resolvedType instanceof protobuf.Type && typesWithInt64.has(field.resolvedType))) {
      typesWithInt64.add(type);
      metadataChanged = true;
    }
  }
}

for (const type of types.filter((candidate) => typesWithInt64.has(candidate))) {
  lines.push(`  '${qualifiedTypeName(type)}': [`);
  for (const field of type.fieldsArray.sort((left, right) => left.id - right.id)) {
    if (isInt64(field.type)) {
      lines.push(`    { name: '${field.name}', repeated: ${field.repeated}, kind: 'int64' },`);
    } else if (field.resolvedType instanceof protobuf.Type && typesWithInt64.has(field.resolvedType)) {
      lines.push(
        `    { name: '${field.name}', repeated: ${field.repeated}, kind: 'message', message_type: '${qualifiedTypeName(field.resolvedType)}' },`
      );
    }
  }
  lines.push('  ],');
}
lines.push(
  '};',
  '',
  'export type ProtocolFieldMetadata =',
  "  | { readonly name: string; readonly repeated: boolean; readonly kind: 'number' | 'string' | 'boolean' | 'bytes' }",
  "  | { readonly name: string; readonly repeated: boolean; readonly kind: 'message'; readonly message_type: string };",
  '',
  'export const PROTOCOL_FIELD_METADATA_BY_TYPE: Readonly<Record<string, readonly ProtocolFieldMetadata[]>> = {'
);

for (const type of types) {
  lines.push(`  '${qualifiedTypeName(type)}': [`);
  for (const field of type.fieldsArray.sort((left, right) => left.id - right.id)) {
    if (field.resolvedType instanceof protobuf.Type) {
      lines.push(
        `    { name: '${field.name}', repeated: ${field.repeated}, kind: 'message', message_type: '${qualifiedTypeName(field.resolvedType)}' },`
      );
    } else {
      lines.push(`    { name: '${field.name}', repeated: ${field.repeated}, kind: '${fieldMetadataKind(field)}' },`);
    }
  }
  lines.push('  ],');
}
lines.push('};');

const typeOutput = `${lines.join('\n')}\n`;
const { runtimeOutput, runtimeTypesOutput } = await generateStaticRuntime(root);
const staticRuntimeOutput = preserveEmptyEnvelopePayload(runtimeOutput)
  .replace(/^\/\*eslint-disable[^\n]*\*\/\r?\n/, '');
const generatedPrefix = [
  '/* eslint-disable */',
  '// Code generated by scripts/generate-protocol.mjs; DO NOT EDIT.',
  `// Canonical schema SHA-256: ${schemaDigest}`,
  ''
].join('\n');
const outputs = [
  [outputFile, typeOutput],
  [runtimeFile, `${generatedPrefix}${staticRuntimeOutput}`],
  [runtimeTypesFile, `${generatedPrefix}${runtimeTypesOutput}`]
];

if (process.argv.includes('--check')) {
  const staleFiles = [];
  for (const [file, expected] of outputs) {
    const current = await readFile(file, 'utf8').catch(() => '');
    if (current !== expected) staleFiles.push(path.relative(repoDir, file));
  }
  if (staleFiles.length > 0) {
    console.error(`Generated Web protocol is stale: ${staleFiles.join(', ')}. Run: npm run proto:generate`);
    process.exitCode = 1;
  }
} else {
  for (const [file, output] of outputs) {
    await writeFile(file, output);
    console.log(`Generated ${path.relative(repoDir, file)}`);
  }
}

function pascalCase(value) {
  return value.toLowerCase().replace(/(^|_)([a-z0-9])/g, (_match, _separator, character) => character.toUpperCase());
}

function typescriptType(field) {
  if (field.resolvedType instanceof protobuf.Type || field.resolvedType instanceof protobuf.Enum) {
    return field.resolvedType.name;
  }
  switch (field.type) {
    case 'double':
    case 'float':
    case 'int32':
    case 'uint32':
    case 'sint32':
    case 'fixed32':
    case 'sfixed32':
    case 'int64':
    case 'uint64':
    case 'sint64':
    case 'fixed64':
    case 'sfixed64':
      return 'number';
    case 'bool':
      return 'boolean';
    case 'string':
      return 'string';
    case 'bytes':
      return 'Uint8Array';
    default:
      throw new Error(`Unsupported protobuf field type: ${field.type}`);
  }
}

function isInt64(type) {
  return ['int64', 'uint64', 'sint64', 'fixed64', 'sfixed64'].includes(type);
}

function qualifiedTypeName(type) {
  return type.fullName.replace(/^\./, '');
}

function fieldMetadataKind(field) {
  if (field.resolvedType instanceof protobuf.Enum || isNumeric(field.type)) return 'number';
  if (field.type === 'string') return 'string';
  if (field.type === 'bool') return 'boolean';
  if (field.type === 'bytes') return 'bytes';
  throw new Error(`Unsupported protobuf field metadata type: ${field.type}`);
}

function isNumeric(type) {
  return [
    'double', 'float',
    'int32', 'uint32', 'sint32', 'fixed32', 'sfixed32',
    'int64', 'uint64', 'sint64', 'fixed64', 'sfixed64'
  ].includes(type);
}

function preserveEmptyEnvelopePayload(output) {
  const generatedCondition = 'if (message.payload != null && $Object.hasOwnProperty.call(message, "payload") && message.payload.length)';
  const requiredCondition = 'if (message.payload != null && $Object.hasOwnProperty.call(message, "payload"))';
  const matches = output.split(generatedCondition).length - 1;
  if (matches !== 1) throw new Error(`Expected one generated envelope payload condition, found ${matches}`);
  return output.replace(generatedCondition, requiredCondition);
}

function generateStaticRuntime(runtimeRoot) {
  return new Promise((resolve, reject) => {
    pbjs.generate(runtimeRoot, {
      target: 'static-module',
      wrap: 'esm',
      dependency: 'protobufjs/minimal.js',
      dts: true,
      'keep-case': true,
      'force-number': true,
      create: false,
      convert: false,
      delimited: false,
      typeurl: false,
      service: false
    }, (error, runtimeOutput, runtimeTypesOutput) => {
      if (error) {
        reject(error);
        return;
      }
      if (typeof runtimeOutput !== 'string' || typeof runtimeTypesOutput !== 'string') {
        reject(new Error('protobufjs-cli did not emit the static runtime and declarations'));
        return;
      }
      resolve({ runtimeOutput, runtimeTypesOutput });
    });
  });
}
